# Operasi Aritmatika

a = 10
b = 3

# Operasi Tambah +
hasil = a + b
print(a, "+", b, "=", hasil)

# Operasi Pengurangan -
hasil = a - b
print(a, "-", b, "=", hasil)

# Operasi Perkalian *
hasil = a * b
print(a, "*", b, "=", hasil)

# Operasi Pembagian /
hasil = a / b
print(a, "/", b, "=", hasil)

# Operasi Eksponen (pangkat) **
hasil = a**b
print(a, "**", b, "=", hasil)

# Operasi Modulus %
hasil = a % b
print(a, "%", b, "=", hasil)

# Operasi floor division //
hasil = a // b
print(a, "//", b, "=", hasil)
