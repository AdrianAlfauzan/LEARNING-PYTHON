# Operasi yang dapat dilakukan dengan penyingkatan
# Operasi ditambah dengan assigment

a = 5  # ini adalah assignmen
print("nilai a =", a)

a += 1  # artinya adalah a = a + 1
print("nilai a += 1, nilai a menjadi : ", a)

a -= 2  # artinya adalah a = a - 2
print("nilai a -= 2, nilai a menjadi : ", a)

a *= 5  # artinya adalah a = a * 5
print("nilai a *= 5, nilai a menjadi : ", a)

a /= 10  # artinya adalah a = a / 10
print("nilai a /= 10, nilai a menjadi : ", a)

# Modulus,eksponen,floor division dll
b = 10
print("\n nilai b = ", b)

b %= 3  # bisa diubah menjadi "%,//,** dll"
print("nilai b %= 3, nilai b menjadi : ", b)


# Operasi bitwise
# OR
c = True
print("\n nilai c = ", c)

c |= False
print("nilai c |= 3, nilai c menjadi : ", c)

# AND
c = True
print("\n nilai c = ", c)

c &= False
print("nilai c &= 3, nilai c menjadi : ", c)

# XOR
c = True
print("\n nilai c = ", c)

c ^= False
print("nilai c ^= 3, nilai c menjadi : ", c)
