# ELIF = Else if
nama = input("Nama anda siapa ? : ")

if nama == "adrian":  # kondisi 1
    print("Adrian adalah seorang programmer")  # aksi true 2
elif nama == "musa":  # kondisi 2
    print("Musa adalah nama kepanjangan dari adrian")  # aksi true 2
elif nama == "alfauzan":  # kondisi 3
    print("alfauzan adalah nama kepanjangan dari adrian juga")  # aksi true 3
else:
    print("Sorry, kamu bukan adrian yang sebenarnya")
