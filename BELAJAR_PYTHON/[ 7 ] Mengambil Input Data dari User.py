# Input User

# Data yang dimasukkan pasti string
data_string = input("Masukkan Data String: ")
print("data = ", data_string)
print("type = ", type(data_string))

# Jika ingin mengambil INT, maka
angka_integer = int(input("Masukkan Data Integer : "))
print("data = ", angka_integer)
print("type = ", type(angka_integer))

# Jika ingin mengambil Float, maka
angka_float = float(input("Masukkan Data Float: "))
print("data = ", angka_float)
print("type = ", type(angka_float))

# Jika ingin mengambil Boolean, maka
data_boolean = bool(int(input("Masukkan Data Boolean: ")))
print("data = ", data_boolean)
print("type = ", type(data_boolean))
